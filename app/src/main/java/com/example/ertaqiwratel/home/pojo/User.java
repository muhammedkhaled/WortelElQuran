package com.example.ertaqiwratel.home.pojo;

public class User {
    private String userType, phone, name, gender, image, birthDate, country, governorate, email, pass,
        state, level, quranAmount , suitableDay, suitableTime, shiekhName;

    public User() {
    }

    public String getUserType() {
        return userType;
    }

    public void setUserType(String userType) {
        this.userType = userType;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getBirthDate() {
        return birthDate;
    }

    public void setBirthDate(String birthDate) {
        this.birthDate = birthDate;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getGovernorate() {
        return governorate;
    }

    public void setGovernorate(String governorate) {
        this.governorate = governorate;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getLevel() {
        return level;
    }

    public void setLevel(String level) {
        this.level = level;
    }

    public String getQuranAmount() {
        return quranAmount;
    }

    public void setQuranAmount(String quranAmount) {
        this.quranAmount = quranAmount;
    }

    public String getSuitableDay() {
        return suitableDay;
    }

    public void setSuitableDay(String suitableDay) {
        this.suitableDay = suitableDay;
    }

    public String getSuitableTime() {
        return suitableTime;
    }

    public void setSuitableTime(String suitableTime) {
        this.suitableTime = suitableTime;
    }

    public String getShiekhName() {
        return shiekhName;
    }

    public void setShiekhName(String shiekhName) {
        this.shiekhName = shiekhName;
    }
}
